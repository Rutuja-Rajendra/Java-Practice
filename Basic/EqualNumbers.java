public class EqualNumbers {
    public static void main(String[] args) {
        
        //Check if two numbers are equal without using ==.

        int num1 = 12;
        int num2 = 10;

        if((num1 - num2) != 0)
        {
            System.out.println("Not Equal");
        }
        else
        {
            System.out.println("Equal");
        }
    }    
}
