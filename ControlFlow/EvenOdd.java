package ControlFlow;

public class EvenOdd {
    
    public static void main(String[] args) {
        
        //Check if a number is even or odd
        
        int num = 181;

        if(num % 2 == 0)
        {
            System.out.println("Even");
        }
        else
        {
            System.out.println("Odd");
        }
    }
}
