package ControlFlow;

public class Ifelse {
    public static void main(String[] args) {
        
        //Check if a number is positive, negative, or zero

        int num = 0;

        if(num == 0)
        {
            System.out.println("Number is zero");
        }
        else if(num > 0)
        {
            System.out.println("Number is positive");
        }
        else
        {
            System.out.println("Number is negative");
        }
    }
}
